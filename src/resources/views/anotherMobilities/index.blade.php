                <p>
                    IVF – International Visegrad Fund (Medzinárodný vyšehradský fond) umožňuje prostredníctvom poskytovaných
                    štipendií realizovať magisterské a post-magisterské (doktorandské alebo post-doktoradské) štúdium alebo výskum
                    na akreditovaných VŠ inštitúciách v regióne V4, rovnako ako v krajinách západného Balkánu a Východného partnerstva.
                    Oprávnenými žiadateľmi sú študenti z Českej republiky, Maďarska, Poľska a Slovenska. Žiadatelia z krajín V4 môžu
                    požiadať o 1 – 4-semestrálny magisterský pobyt alebo 2-semestrálny doktorandský/postdoktorandský pobyt na akreditovanej
                    vysokoškolskej inštitúcii v krajine V4 („Intra-Visegrad scheme“) a v krajine západného Balkánu a Východného partnerstva
                    („Outgoing scheme“). Prijímajúcou krajinou pobytu však nesmie byť krajina, v ktorej má študent bydlisko, alebo tam predtým študoval.
                    Program IVF nie je vhodný pre realizáciu štúdia v rámci spoločného študijného programu typu double alebo joint degree.
                    Študent môže požiadať o samostatný cestovný grant, ak je vzdialenosť jeho bydliska od miesta pobytu v prijímajúcej krajine viac ako 1500 km.
